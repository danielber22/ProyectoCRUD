package modelo;

/**
 *
 * @author Daniel
 */
public class Personas {
    int id;
    String nombre;
    int edad;
    String pais;

    public Personas() {
    }

    public Personas(int id, String nombre, int edad, String pais) {
        this.id = id;
        this.nombre = nombre;
        this.edad = edad;
        this.pais = pais;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }
    
}
